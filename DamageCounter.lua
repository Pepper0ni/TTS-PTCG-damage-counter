function onLoad(state)
 self.max_typed_number=40
 if tonumber(state)then curCounter=state end
 if self.GetCustomObject().image!=listOfCounters[tonumber(curCounter)]then changeArt()end
end

function changeArt()
 self.setCustomObject({image=listOfCounters[tonumber(curCounter)]})
 self.setName(curCounter.."0 Damage")
 self.script_state=curCounter
 self.reload()
end

function onNumberTyped(player_color,number)
 if number>0 and number<=#listOfCounters then
  curCounter=tostring(number)
  changeArt()
 end
end
